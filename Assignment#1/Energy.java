public class Energy
{

  public static void main(String[] args)
  
  {
	  
	  double Energy , mass , speedofLight;
	  
	  mass = 20;
	  
	  
	  // Speed Of Light in Meters 
	  
	  speedofLight = 300000 * 1000;
	  
	  // Einsteins Equation E = mc2
	  
	  Energy = mass * speedofLight;
	  
	  System.out.println("Enter Mass in Kilograms :  " + mass + " Kg ");

	  System.out.println("EnergyProduced :  " + Energy + " Joules ");
	  
  }
  
}
	  
	  
	  