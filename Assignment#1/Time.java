public class Time{
	
  public static void main(String[] args){
  
   
  
     double oneDegree , timeTaken , angleinDegree;
	 
	 
	 // Earth takes 240 seconds to rotate one degree angle, 
	 // that will be 4 minutes preciesly. 
	 
	 oneDegree = 240/60;
	 
	 // How much time will it take for 16.18 degree angle ?

     angleinDegree = 16.18;

     timeTaken = oneDegree * angleinDegree;

     System.out.println("Enter the angle in degree :  " + angleinDegree + " Degrees ");

     System.out.println("Time :  " + timeTaken + " minutes ");
	 
  }
}	 
